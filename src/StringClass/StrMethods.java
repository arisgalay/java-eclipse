package StringClass;

public class StrMethods {

	public static void main(String[] args) {
//		String = a reference data type that can store on or more characters
//		         reference data type have access to useful methods
	
		String name = "Aris";
//		Methods:
//		length - return the length of a string
//		equals - Compare string value (Case Sensitive)
//		equalsIgnorCase- Compare string value (Non-case Sensitive)
//		charAt - return character of a specific index
//		indexOf - return index of a specific character
//		toUpperCase - return a string value to upper case
//		toLowerCase - return a string value to lower case
//		trim - remove all empty space before and after
//		replace - replace a character of an string
		System.out.println(name.isBlank());
		
	}

}
